	if(f < 0) neg = 1;
	else neg = 0;
	f = abs(f);
	int int_part = floor(f);
	if(neg) {
		cout << "$(";
	}else cout << "$";

	string s = to_string(int_part);
	for(int i=0; i<s.length(); i++) {
		if( i!=0 && (s.length()-i)%3 == 0 ) cout << ",";
		cout << s[i];
	} 

	int float_part = floor( (f-floor(f))*100);
	cout << ".";
